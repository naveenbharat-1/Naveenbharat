CREATE OR REPLACE FUNCTION public.check_rate_limit(_bucket text, _user_id uuid, _max integer, _window_seconds integer)
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
  _window_start timestamptz;
  _current_count integer;
BEGIN
  _window_start := to_timestamp(
    (floor(extract(epoch from now())::bigint / _window_seconds) * _window_seconds)
  );

  INSERT INTO public.rate_limits (bucket, user_id, window_start, count)
  VALUES (_bucket, _user_id, _window_start, 1)
  ON CONFLICT (bucket, user_id, window_start)
  DO UPDATE SET count = public.rate_limits.count + 1
  RETURNING count INTO _current_count;

  DELETE FROM public.rate_limits
   WHERE window_start < now() - (_window_seconds * 4 || ' seconds')::interval;

  RETURN _current_count <= _max;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.check_rate_limit_text(_bucket text, _identifier text, _max integer, _window_seconds integer)
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
  _uid uuid;
BEGIN
  -- Deterministic uuid v5-ish from bucket+identifier so different IPs get distinct rows.
  _uid := ('00000000-0000-0000-0000-' || substr(md5(_bucket || ':' || _identifier), 1, 12))::uuid;
  RETURN public.check_rate_limit(_bucket, _uid, _max, _window_seconds);
END;
$function$
;

CREATE OR REPLACE FUNCTION public.complete_paid_enrollment(_user_id uuid, _course_id bigint, _razorpay_order_id text, _razorpay_payment_id text)
 RETURNS bigint
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
  _payment_id bigint;
  _enrollment_id bigint;
BEGIN
  SELECT id INTO _payment_id
  FROM public.razorpay_payments
  WHERE razorpay_order_id = _razorpay_order_id
    AND user_id = _user_id
    AND course_id = _course_id
  FOR UPDATE;

  IF _payment_id IS NULL THEN
    RAISE EXCEPTION 'Payment record not found for order/user/course' USING ERRCODE = 'P0002';
  END IF;

  UPDATE public.razorpay_payments
     SET razorpay_payment_id = _razorpay_payment_id,
         status = 'completed',
         updated_at = now()
   WHERE id = _payment_id;

  INSERT INTO public.enrollments (user_id, course_id, status, purchased_at)
  VALUES (_user_id, _course_id, 'active', now())
  ON CONFLICT (user_id, course_id) DO UPDATE
     SET status = 'active',
         purchased_at = COALESCE(public.enrollments.purchased_at, EXCLUDED.purchased_at)
  RETURNING id INTO _enrollment_id;

  -- Audit trail
  INSERT INTO public.audit_log (user_id, action, table_name, record_count, metadata)
  VALUES (
    _user_id,
    'enrollment_completed',
    'enrollments',
    1,
    jsonb_build_object(
      'course_id', _course_id,
      'enrollment_id', _enrollment_id,
      'razorpay_order_id', _razorpay_order_id,
      'razorpay_payment_id', _razorpay_payment_id
    )
  );

  RETURN _enrollment_id;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.enforce_message_recipient_readonly()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
BEGIN
  -- Admins bypass
  IF auth.uid() IS NULL OR public.has_role(auth.uid(), 'admin'::app_role) THEN
    RETURN NEW;
  END IF;

  -- If the caller is the recipient but NOT the sender, they may only change is_read.
  IF auth.uid() = OLD.recipient_id AND auth.uid() IS DISTINCT FROM OLD.sender_id THEN
    IF NEW.sender_id      IS DISTINCT FROM OLD.sender_id
    OR NEW.recipient_id   IS DISTINCT FROM OLD.recipient_id
    OR NEW.subject        IS DISTINCT FROM OLD.subject
    OR NEW.content        IS DISTINCT FROM OLD.content
    OR NEW.attachment_url IS DISTINCT FROM OLD.attachment_url
    OR NEW.attachment_type IS DISTINCT FROM OLD.attachment_type
    OR NEW.created_at     IS DISTINCT FROM OLD.created_at
    THEN
      RAISE EXCEPTION 'Recipients may only mark messages as read'
        USING ERRCODE = '42501';
    END IF;
  END IF;

  RETURN NEW;
END;
$function$
;

