CREATE OR REPLACE FUNCTION public.get_quiz_questions(_quiz_id uuid)
 RETURNS TABLE(id uuid, quiz_id uuid, question_text text, question_type text, options jsonb, marks integer, negative_marks integer, order_index integer, image_url text)
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
  _uid uuid := auth.uid();
  _course_id bigint;
  _created_by uuid;
BEGIN
  IF _uid IS NULL THEN
    RAISE EXCEPTION 'Authentication required' USING ERRCODE='42501';
  END IF;

  -- Admins bypass.
  IF public.has_role(_uid, 'admin'::public.app_role) THEN
    RETURN QUERY
      SELECT q.id, q.quiz_id, q.question_text, q.question_type,
             q.options, q.marks, q.negative_marks, q.order_index, q.image_url
      FROM public.questions q
      WHERE q.quiz_id = _quiz_id
      ORDER BY q.order_index;
    RETURN;
  END IF;

  SELECT qz.course_id, qz.created_by
    INTO _course_id, _created_by
    FROM public.quizzes qz
    WHERE qz.id = _quiz_id;

  IF _course_id IS NULL AND _created_by IS NULL THEN
    RAISE EXCEPTION 'Quiz not found' USING ERRCODE='P0002';
  END IF;

  -- Quiz owner (teacher) always allowed.
  IF _created_by = _uid THEN
    -- fall through to return
    NULL;
  -- Enrolled student allowed when quiz is tied to a course.
  ELSIF _course_id IS NOT NULL AND EXISTS (
    SELECT 1 FROM public.enrollments e
    WHERE e.user_id = _uid
      AND e.course_id = _course_id
      AND e.status = 'active'
  ) THEN
    NULL;
  ELSE
    RAISE EXCEPTION 'Not authorized for this quiz' USING ERRCODE='42501';
  END IF;

  RETURN QUERY
    SELECT q.id, q.quiz_id, q.question_text, q.question_type,
           q.options, q.marks, q.negative_marks, q.order_index, q.image_url
    FROM public.questions q
    WHERE q.quiz_id = _quiz_id
    ORDER BY q.order_index;
END;
$function$
;

