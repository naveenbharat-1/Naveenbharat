CREATE OR REPLACE FUNCTION public.lock_submitted_quiz_attempt()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
BEGIN
  IF auth.uid() IS NULL OR public.has_role(auth.uid(), 'admin'::app_role) THEN
    RETURN NEW;
  END IF;

  -- Once submitted, block any student-side mutation of score fields
  IF OLD.submitted_at IS NOT NULL THEN
    IF NEW.score        IS DISTINCT FROM OLD.score
    OR NEW.percentage   IS DISTINCT FROM OLD.percentage
    OR NEW.passed       IS DISTINCT FROM OLD.passed
    OR NEW.submitted_at IS DISTINCT FROM OLD.submitted_at THEN
      RAISE EXCEPTION 'Submitted quiz attempts cannot be modified'
        USING ERRCODE = '42501';
    END IF;
  END IF;
  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.match_knowledge(query_embedding extensions.vector, match_threshold double precision DEFAULT 0.5, match_count integer DEFAULT 5)
 RETURNS TABLE(id uuid, title text, content text, category text, similarity double precision)
 LANGUAGE sql
 STABLE SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
  SELECT kb.id, kb.title, kb.content, kb.category,
    (1 - (kb.embedding OPERATOR(extensions.<=>) query_embedding))::float AS similarity
  FROM public.knowledge_base kb
  WHERE kb.is_active = true
    AND kb.embedding IS NOT NULL
    AND (1 - (kb.embedding OPERATOR(extensions.<=>) query_embedding)) > match_threshold
  ORDER BY kb.embedding OPERATOR(extensions.<=>) query_embedding
  LIMIT match_count;
$function$
;

CREATE OR REPLACE FUNCTION public.prevent_enrollment_status_tampering()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
BEGIN
  -- Admins and service_role bypass.
