        SELECT id, subject, scheduled_at, zoom_join_url, status
        FROM public.doubt_sessions
        WHERE student_id = _uid AND status IN ('scheduled', 'active')
        ORDER BY scheduled_at ASC
        LIMIT 3
      ) ds
    ), '[]'::jsonb)
  ) INTO _result;

  RETURN _result;
END;
$function$
