      FROM (
        SELECT * FROM public.quiz_attempts
        WHERE user_id = _uid AND submitted_at IS NOT NULL
        ORDER BY created_at DESC
        LIMIT 10
      ) qa
      LEFT JOIN public.quizzes qz ON qz.id = qa.quiz_id
