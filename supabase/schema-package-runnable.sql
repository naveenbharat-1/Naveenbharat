      SELECT jsonb_agg(jsonb_build_object(
        'id', ch.id, 'code', ch.code, 'title', ch.title, 'parent_id', ch.parent_id
      ) ORDER BY ch.position ASC NULLS LAST)
      FROM public.chapters ch
